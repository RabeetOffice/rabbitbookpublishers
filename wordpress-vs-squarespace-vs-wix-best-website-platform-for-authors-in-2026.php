                    <h2 class="section__title"><span>Squarespace for Authors: Pros & Cons</span></h2>
                    <p><strong>Pros:</strong></p>
                    <ul class="list">
                        <li>Beautiful Templates – Polished designs for covers, bios, media.</li>
                        <li>All-in-One Hosting – No worries about updates, patches, or security.</li>
                        <li>Built-in Blogging, E-commerce, Email Tools.</li>
                    </ul>
                    <p><strong>Cons:</strong></p>
                    <ul class="list">
                        <li>Less Flexibility – Limited for deep customization.</li>
                        <li>SEO & Performance – Restricted server speed & metadata control.</li>
                        <li>Higher Cost – Premium plans for advanced features.</li>
                    </ul>
                    <p><strong>Best for Authors Who:</strong> Value design, minimal technical work, and reliable tools.
                    </p>
                </section>

                <section class="section">
                    <h2 class="section__title"><span>Wix for Authors: Pros & Cons</span></h2>
                    <p><strong>Pros:</strong></p>
                    <ul class="list">
                        <li>Drag-and-drop ease, very quick setup.</li>
                        <li>Affordable entry plans, free tier available.</li>
                        <li>Great for simple one-book or small sites.</li>
                    </ul>
                    <p><strong>Cons:</strong></p>
                    <ul class="list">
                        <li>Migration & lock-in issues, templates non-transferable.</li>
                        <li>Performance can lag; design clutter risk.</li>
                        <li>Scaling limitations for large catalogs or advanced ecommerce.</li>
                    </ul>
                    <p><strong>Best for Authors Who:</strong> Are beginners, want fast visibility, or a budget-friendly
                        option.</p>
