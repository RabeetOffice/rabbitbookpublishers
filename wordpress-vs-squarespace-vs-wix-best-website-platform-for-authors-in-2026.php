                    <h2 class="section__title"><span>WordPress for Authors: Pros & Cons</span></h2>
                    <p><strong>Pros:</strong></p>
                    <ul class="list">
                        <li>Flexibility & Plugin Ecosystem – SEO, ecommerce, memberships, events, etc.</li>
                        <li>Strong SEO Potential – Full control over speed, metadata, structured data.</li>
                        <li>Scalability – Supports many books, blogs, downloads.</li>
                        <li>Control over Hosting & Design – Choose hosting, theme, custom design.</li>
                    </ul>
                    <p><strong>Cons:</strong></p>
                    <ul class="list">
                        <li>Learning Curve – Hosting, updates, plugin conflicts require effort.</li>
                        <li>Costs Vary – Premium themes, good hosting, plugins add up.</li>
                        <li>Performance – Bad hosting/themes can slow site.</li>
                    </ul>
                    <p><strong>Best for Authors Who:</strong> Want blogs, mailing lists, ecommerce, events, and don’t
                        mind setup & upkeep.</p>
                </section>

