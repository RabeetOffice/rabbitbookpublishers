					<h3 class="section__subtitle">Advantages of Self-Publishing:</h3>
					<ul class="list">
						<li>Global reach via eBooks and print.</li>
						<li>Control over your brand.</li>
						<li>Higher royalties than traditional publishing.</li>
					</ul>
					<h3 class="section__subtitle">Disadvantages of Self-Publishing:</h3>
					<ul class="list">
						<li>The marketing burden falls on you.</li>
						<li>Self-publishing distribution challenges make it hard to get into physical stores.</li>
						<li>Requires upfront investment in editing and design.</li>
					</ul>
					<h3 class="section__subtitle">Advantages of Substack:</h3>
					<ul class="list">
						<li>Easy to start, no upfront costs.</li>
						<li>Perfect for fiction serialization on Substack and reader engagement.</li>
						<li>Direct ownership of your email list.</li>
					</ul>
					<h3 class="section__subtitle">Disadvantages of Substack:</h3>
					<ul class="list">
						<li>Slow subscriber growth.</li>
						<li>Limited design customization.</li>
						<li>Reliant on keeping up with frequent posts.</li>
					</ul>
