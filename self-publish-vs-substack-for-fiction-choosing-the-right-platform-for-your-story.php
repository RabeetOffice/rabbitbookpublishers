					<h2 class="section__title"><span>Understanding the Platforms</span></h2>
					<h3 class="section__subtitle">2.1 Self-Publishing in Today’s Market</h3>
					<p>Self-publishing means uploading your book directly to platforms like Amazon KDP or IngramSpark.
						You keep control of editing, cover design, and pricing. Royalties can be as high as 70%,
						However, the difficulties of self-publishing distribution, getting to bookstores and
						distinguishing out amid millions of titles, are genuine obstacles. This important element is
						overlooked by many blogs.</p>
					<h3 class="section__subtitle">2.2 What Substack Offers for Fiction Writers</h3>
					<p>Substack is a newsletter-based publishing platform where you can release serialized stories and
						monetize through subscriptions. The rise of fiction serialization on Substack has revived an old
						tradition: episodic storytelling once common in magazines. Unlike traditional publishing, you
						don’t need permission or a contract, it’s just you and your readers. But monetization starts
						slow, and discoverability is tied to Substack’s network.</p>
				</section>
				<section class="section">
					<h2 class="section__title"><span>Self-Publishing vs Newsletter Platforms: Key Differences</span>
					</h2>
					<p>This is where the comparison between self-publishing vs newsletter platforms gets interesting.
					</p>
					<ul class="list">
						<li><strong>Audience Reach:</strong> Self-publishing offers global access to Amazon and
							bookstores, while newsletters target niche but highly engaged readers.</li>
						<li><strong>Monetization:</strong> Self-publishing pays per copy sold. Substack relies on
							subscriptions, with 10% fees.</li>
						<li><strong>Creative Flexibility:</strong> In self-publishing, your work must be polished. On
							Substack, readers often enjoy works in progress.</li>
						<li><strong>Marketing Effort:</strong> Both require work, but in different ways, SEO, reviews,
							and ads for self-publishing vs community engagement and recommendations on Substack.</li>
					</ul>
				</section>
