                    <h2 class="section__title"><span>Best Practices When Creating an AR Book</span></h2>
                    <ul class="list">
                        <li>Begin with the story & illustrations first, then layer AR.</li>
                        <li>Choose illustrators & animators experienced in AR design.</li>
                        <li>Test with your target audience early to prevent costly revisions.</li>
                        <li>Plan for updates and maintenance in your budget.</li>
                        <li>Keep features meaningful; avoid adding AR just for novelty.</li>
                    </ul>
                </section>

